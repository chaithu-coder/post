const mongoose = require('mongoose');

const userSchemaData = new mongoose.Schema({
    fname: {
        type: String,
        required: true,
    },
    lname: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    phone:{
        type: String,
        required: true,
    },
    password: {
        type: String,
        required: true,
    },
    cpassword: {
        type: String,
        required: true,
    },
    Date: {
        type: Date,
        default : new Date()
    } 
});

module.exports = mongoose.model('User', userSchemaData);